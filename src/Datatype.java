public class Datatype {
    public static void main(String[] args) {
        boolean b=true;
        System.out.println(b);
        char c='A';
        System.out.println(c);
        int i=23;
        System.out.println(i);
        float f=3.5f;
        System.out.println(f);
        double d=0.10;
        System.out.println(d);
    }
}
